/**
 * 翻译函数
 * @param {string} key 键值
 * @param {object} params 替换参数
 */
export function t(key, params = {}) {
    const lang = config.get("lang") || "en";
    let text = translations[lang][key] || translations["en"][key] || key;
    
    // 简单的占位符替换
    Object.keys(params).forEach(p => {
        text = text.replace(`{${p}}`, params[p]);
    });
    
    return text;
}
